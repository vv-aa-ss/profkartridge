package com.example.bits_helper

/**
 * ИНСТРУКЦИЯ ПО НАСТРОЙКЕ СИНХРОНИЗАЦИИ С ЯНДЕКС.ДИСКОМ
 * 
 * Для работы синхронизации базы данных между несколькими устройствами
 * необходимо настроить доступ к Яндекс.Диску через OAuth токен.
 * 
 * ШАГ 1: Получение OAuth токена
 * 
 * 1. Перейдите на https://oauth.yandex.ru/
 * 2. Нажмите "Создать приложение"
 * 3. Заполните форму:
 *    - Название: "Bits Helper Sync"
 *    - Описание: "Синхронизация базы данных картриджей"
 *    - Платформы: выберите "Веб-сервисы"
 *    - Callback URI: https://oauth.yandex.ru/verification_code
 * 4. После создания приложения скопируйте "ID приложения"
 * 
 * ШАГ 2: Получение токена доступа
 * 
 * 1. Откройте браузер и перейдите по ссылке:
 *    https://oauth.yandex.ru/authorize?response_type=token&client_id=ВАШ_APP_ID
 *    (замените ВАШ_APP_ID на ID приложения из шага 1)
 * 2. Войдите в свой аккаунт Яндекс
 * 3. Разрешите доступ к Яндекс.Диску
 * 4. Скопируйте токен из адресной строки (после access_token=)
 * 
 * ШАГ 3: Настройка в приложении
 * 
 * 1. Откройте приложение Bits Helper
 * 2. Нажмите кнопку синхронизации (иконка облака) в верхней части экрана
 * 3. Введите полученный токен доступа
 * 4. Нажмите "Синхронизировать"
 * 
 * ШАГ 4: Использование на других устройствах
 * 
 * 1. Установите APK на другое устройство
 * 2. Повторите шаги 1-3 для получения токена
 * 3. Введите тот же токен в приложении
 * 4. Теперь базы данных будут синхронизироваться между устройствами
 * 
 * ВАЖНЫЕ МОМЕНТЫ:
 * 
 * - Токен действует ограниченное время (обычно 1 год)
 * - При истечении токена нужно получить новый
 * - Синхронизация происходит в фоновом режиме
 * - При конфликтах данных приложение предложит варианты разрешения
 * - Резервные копии создаются автоматически перед синхронизацией
 * 
 * БЕЗОПАСНОСТЬ:
 * 
 * - Не передавайте токен третьим лицам
 * - Храните токен в безопасном месте
 * - При подозрении на компрометацию получите новый токен
 * 
 * ПОДДЕРЖКА:
 * 
 * При возникновении проблем с синхронизацией:
 * 1. Проверьте интернет-соединение
 * 2. Убедитесь, что токен действителен
 * 3. Попробуйте получить новый токен
 * 4. Обратитесь к разработчику приложения
 */
